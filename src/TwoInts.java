public class TwoInts {
    int keyInt;
    int sonInt;

    public TwoInts() {}

    public TwoInts(int keyInt, int sonInt) {
        this.keyInt = keyInt;
        this.sonInt = sonInt;
    }

    public int getKeyInt() {
        return keyInt;
    }

    public int getSonInt() {
        return sonInt;
    }

    public void setKeyInt(int keyInt) {
        this.keyInt = keyInt;
    }

    public void setSonInt(int sonInt) {
        this.sonInt = sonInt;
    }
}
